<h2>Questions les plus fréquentes</h2><br>
<fieldset>
<legend><h2>Qui a accès à mes données?</h2></legend>
<p>Les données de consommation générale des habitations peuvent etre communiquées au propriétaire du bail si vous louez votre habitation.</p>
</fieldset>
<br><br>
<fieldset>
<legend><h2>Comment bien installer un capteur thermique ?</h2><br></legend>
<p>Il est important de placer le capteur thermique à une grande distance d'un radiateur, pour que celui-ci puisse capter la température ambiante de la salle.</p></fieldset>
<br><br>
<fieldset>
<legend><h2>Comment bien installer un capteur de pression ?</h2><br></legend>
<p>Il est important de placer les capteurs de pression sur les portes et fenètres principales de la maison.</p></fieldset>
<br><br>

<fieldset>
<legend><h2>Comment nous contacter?</h2><br></legend>
<p>Dans la barre horizontale en haut de page, cliquez sur l'onglet nous contacter et choisissez le moyen de communication qui vous convient le mieux.</p></fieldset>
<br><br>

<fieldset>
<legend><h2>Comment programmer un mode ?</h2><br></legend>
<p>Dans la barre verticale, cliquez sur programmation, vous pourrez alors choisir un intervalle de temps et une température à respecter pendant cet intervalle.</p></fieldset>
<br><br>

<fieldset>
<legend><h2>Comment paramétrer ses capteurs?</h2><br></legend>
<p>Dans la barre verticale, cliquez sur l'onglet paramétrage et choisissez la piece et ainsi vous aurez accès à vos capteurs vous pourrez ainsi les modifier.</p></fieldset>
<br><br>

<fieldset>
<legend><h2>Comment modifier ses informations personnelles?</h2><br></legend>
<p>Dans la barre horizontale en haut de page, cliquez sur l'onglet mon profit, vous devrez y ajouter votre nom et votre prénom,quelques informations supplémentaires et la possibilité de modifier votre mot de passe.</p></fieldset>

<br><br>

<fieldset>
<legend><h2>Comment modifier les informations de sa maison?</h2><br></legend>
<p>Dans la barre horizontale en haut de page, cliquez sur l'onglet ma maison et vous pourrez ainsi ajouter un pièce et ajouter un capteur dans cette dernière.</p></fieldset>